/**
 * The set of HTTP verbs the scheduler understands. Used by the default
 * `maxAttemptsFor` policy (5 for GET, 1 for everything else) and by
 * `defaultIsRetryable` (2xx success, 408/429/5xx retry, other 4xx
 * terminal).
 */
export type HttpVerb = 'GET' | 'POST' | 'PUT' | 'PATCH' | 'DELETE';

/**
 * A call handed to the scheduler. The caller provides the
 * `execute` closure (which wraps the platform adapter) and a
 * `connector_id` (used as the `sync_op` row key). The
 * `idempotency_key` is optional — if absent, the scheduler
 * generates a UUID v7 per call.
 */
export interface SchedulerCall {
  readonly tenant_id: string;
  readonly platform: PlatformId;
  readonly connector_id: string;
  readonly verb: HttpVerb;
  /** Idempotency-Key (UUID v7). Generated by the scheduler if omitted. */
  readonly idempotency_key?: string;
  /** Execute the platform call. The Idempotency-Key is passed in. */
  readonly execute: (idempotency_key: string) => Promise<PlatformCallResultForBackoff>;
  /** Override the per-verb max attempts (default: 5 for GET, 1 otherwise). */
  readonly max_attempts?: number;
  /** Override the retryability test (default: 2xx ok, 408/429/5xx retry). */
  readonly is_retryable?: (result: PlatformCallResultForBackoff) => boolean;
  /** Free-form metadata for the audit trail. */
  readonly metadata?: Readonly<Record<string, unknown>>;
}

/**
 * The platform-call result shape the scheduler needs. Mirrors the
 * `PlatformCallResult` in `outbound.ts` — kept as a separate type so
 * this module has no circular dependency on the three-layer limiter.
 */
export interface PlatformCallResultForBackoff {
  readonly status: number;
  readonly headers: Readonly<Record<string, string>>;
  readonly body: unknown;
}

/** What `execute()` resolves with. */
export interface ExecutionResult {
  readonly result: PlatformCallResultForBackoff;
  readonly attempts: number;
  /** True if at least one retry occurred. */
  readonly retried: boolean;
  /** True if the retry budget was exhausted (final status is non-2xx and retryable). */
  readonly exhausted: boolean;
  /** The Idempotency-Key used for the final call (generated if caller didn't pass one). */
  readonly idempotency_key: string;
  /** True if the result was served from the `sync_op` cache (no network call). */
  readonly cached: boolean;
  /** Per-attempt backoff_ms (length = attempts - 1, excludes the first call). */
  readonly backoff_ms: readonly number[];
}

export interface BackoffSchedulerOpts {
  /** Override the default BackoffPolicy. */
  readonly policy?: BackoffPolicy;
  /** Override the default SyncOpStore. Defaults to InMemorySyncOpStore. */
  readonly sync_op?: SyncOpStore;
  /** Audit sink. Defaults to NoopAuditSink. */
  readonly audit?: AuditSink;
  /** `now()` injection for tests. */
  readonly now?: () => number;
  /** Sleep injection for tests. Default: `setTimeout`-backed. */
  readonly sleep?: (ms: number) => Promise<void>;
  /** UUID v7 source. Default: `uuidV7()`. */
  readonly uuid?: () => string;
  /** Pre-built queue. Default: empty TenantWeightedFifo owned by the scheduler. */
  readonly queue?: TenantWeightedFifo<SchedulerCall>;
}

// ---------------------------------------------------------------------------
// Tenant-weighted FIFO
// ---------------------------------------------------------------------------

/**
 * Per-tenant FIFO with weighted round-robin drain (FORA-518).
 *
 * "Weighted" means the tenant with the most headroom is preferred
 * on each pull. Per FORA-487 charter §"Cross-tenant fairness":
 *
 *     Weight = min(rpm_remaining, concurrent_max - concurrent_in_flight)
 *
 * When no weight source is provided (the BackoffScheduler default,
 * or any pre-FORA-518 caller), the FIFO falls back to strict
 * round-robin — which is the simplest fairness guarantee that
 * satisfies "high-retry tenant must not starve a quiet one". The
 * round-robin path is preserved so the existing BackoffScheduler
 * tests stay green.
 *
 * Pending items within a single tenant are served FIFO. Tenants are
 * visited in insertion order (the order of their first enqueue), with
 * the cursor wrapping around so a steady stream from tenant-A does
 * not skip a quiet tenant-B that has only one pending item.
 */
export class TenantWeightedFifo<T> {
  private readonly queues = new Map<string, T[]>();
  /** Insertion order of tenant ids (stable round-robin schedule). */
  private readonly order: string[] = [];
  private cursor = 0;
  /**
   * Weight source for the FORA-518 weighted mode. When null,
   * `pull()` uses strict round-robin. When set, the source maps
   * tenant_id → headroom (any non-negative number, where higher =
   * more headroom). Tenants missing from the source get weight 0.
   */
  private readonly weight_source: ((tenant_id: string) => number) | null;

  constructor(opts: { weight_source?: ((tenant_id: string) => number) | null } = {}) {
    this.weight_source = opts.weight_source ?? null;
  }

  /** Add an item to a tenant's FIFO. O(1). */
  enqueue(tenant_id: string, item: T): void {
    if (!this.queues.has(tenant_id)) {
      this.queues.set(tenant_id, []);
      this.order.push(tenant_id);
    }
    this.queues.get(tenant_id)!.push(item);
  }

  /**
   * Pull the next item. Returns `null` when the queue is empty.
   *
   * - Strict-round-robin mode (no `weight_source`): the cursor walks
   *   tenants in insertion order. After returning, the cursor
   *   advances past the tenant that was just drained.
   * - Weighted mode (FORA-518): the tenant with the highest weight
   *   is chosen. The cursor still advances past the drained tenant
   *   so a steady stream from a heavy tenant does not starve a
   *   weight-0 tenant whose weight later rises (recovering the
   *   "quiet tenant gets its turn" guarantee).
   */
  pull(): { tenant_id: string; item: T } | null {
    if (this.order.length === 0) return null;
    // Garbage-collect stale `order` slots first (idempotent on every
    // pull). The cursor may have to be re-clamped if the GC shrinks
    // the array.
    for (let i = this.order.length - 1; i >= 0; i--) {
      const tenant_id = this.order[i]!;
      if (!this.queues.has(tenant_id) || this.queues.get(tenant_id)!.length === 0) {
        this.queues.delete(tenant_id);
        this.order.splice(i, 1);
      }
    }
    if (this.order.length === 0) return null;
    if (this.cursor >= this.order.length) this.cursor = 0;

    // Pick the tenant: weighted (highest headroom) or strict RR.
    let pick_idx: number;
    if (this.weight_source === null) {
      pick_idx = this.cursor;
    } else {
      let best_idx = -1;
      let best_weight = -Infinity;
      // Tie-break: cursor wins. So the weighted scheduler is stable
      // when weights are equal — a steady stream from tenant-A does
      // not skip a quiet tenant-B whose weight is the same.
      for (let i = 0; i < this.order.length; i++) {
        const idx = (this.cursor + i) % this.order.length;
        const tenant_id = this.order[idx]!;
        const q = this.queues.get(tenant_id);
        if (q === undefined || q.length === 0) continue;
        const w = this.weight_source(tenant_id);
        if (w > best_weight) {
          best_weight = w;
          best_idx = idx;
        }
      }
      if (best_idx === -1) return null;
      pick_idx = best_idx;
    }

    const tenant_id = this.order[pick_idx]!;
    const q = this.queues.get(tenant_id)!;
    const item = q.shift()!;
    // Advance cursor past the drained tenant so a steady stream from
    // a heavy tenant does not bypass a quiet one.
    this.cursor = (pick_idx + 1) % this.order.length;
    if (q.length === 0) {
      this.queues.delete(tenant_id);
      this.order.splice(pick_idx, 1);
      if (this.cursor > pick_idx || this.cursor >= this.order.length) {
        this.cursor = this.cursor > 0 ? this.cursor - 1 : 0;
      }
    }
    return { tenant_id, item };
  }

  /** Total pending items across all tenants. */
  size(): number {
    let n = 0;
    for (const q of this.queues.values()) n += q.length;
    return n;
  }

  /** Number of distinct tenants with pending items. */
  tenantCount(): number {
    return this.queues.size;
  }

  /** Per-tenant pending counts (test / observability seam). */
  pendingByTenant(): Readonly<Record<string, number>> {
    const out: Record<string, number> = {};
    for (const [k, v] of this.queues) out[k] = v.length;
    return out;
  }

  /** Drop all pending items (test / shutdown seam). */
  clear(): void {
    this.queues.clear();
    this.order.length = 0;
    this.cursor = 0;
  }
}

