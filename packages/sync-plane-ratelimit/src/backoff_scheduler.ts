/** Per-verb default retry budget. */
const IDEMPOTENT_VERBS: ReadonlySet<HttpVerb> = new Set(['GET']);

/** Default per-verb max attempts. */
function defaultMaxAttempts(call: SchedulerCall): number {
  return IDEMPOTENT_VERBS.has(call.verb) ? 5 : 1;
}

/** Default retryability test: 2xx ok, 408/429/5xx retry, other 4xx terminal. */
export function defaultIsRetryable(result: PlatformCallResultForBackoff): boolean {
  if (result.status >= 200 && result.status < 300) return false; // success — no retry
  if (result.status === 408 || result.status === 429) return true;
  if (result.status >= 500 && result.status < 600) return true;
  return false;
}

const DEFAULT_SLEEP = (ms: number): Promise<void> =>
  new Promise((resolve) => {
    if (ms <= 0) {
      resolve();
      return;
    }
    setTimeout(resolve, ms);
  });

export class BackoffScheduler {
  private readonly policy: BackoffPolicy;
  private readonly sync_op: SyncOpStore;
  private readonly audit: AuditSink;
  private readonly now: () => number;
  private readonly sleep: (ms: number) => Promise<void>;
  private readonly uuid: () => string;
  private readonly queue: TenantWeightedFifo<SchedulerCall>;

  constructor(opts: BackoffSchedulerOpts = {}) {
    this.policy = opts.policy ?? new BackoffPolicy();
    this.sync_op = opts.sync_op ?? new InMemorySyncOpStore({ now: opts.now ?? Date.now });
    this.audit = opts.audit ?? new NoopAuditSink();
    this.now = opts.now ?? Date.now;
    this.sleep = opts.sleep ?? DEFAULT_SLEEP;
    this.uuid = opts.uuid ?? (() => uuidV7({ now: this.now }));
    this.queue = opts.queue ?? new TenantWeightedFifo<SchedulerCall>();
  }

  /** The retry policy (test seam). */
  get policyInstance(): BackoffPolicy {
    return this.policy;
  }

  /** The dedupe store (test seam). */
  get syncOpStore(): SyncOpStore {
    return this.sync_op;
  }

  /** The fairness queue (test seam). */
  get pendingQueue(): TenantWeightedFifo<SchedulerCall> {
    return this.queue;
  }

  /**
   * Run a call through the retry loop. Sleeps between attempts.
   * Resolves with the final outcome — never rejects (the platform
   * result is captured in `result.status` and `result.body`).
   *
   * Order of operations per attempt:
   *   1. Pre-call: check `sync_op` for `(connector_id, idempotency_key)`.
   *      If hit → return cached result, mark `cached: true`.
   *   2. Invoke the caller's `execute(idempotency_key)`.
   *   3. If status is retryable AND attempts < max → sleep
   *      `policy.nextDelayMs(attempt, result.headers)`, emit
   *      `connector.backoff.retried`, retry.
   *   4. Otherwise → persist to `sync_op` and resolve.
   *   5. If the retry budget was exhausted on a non-2xx → emit
   *      `connector.backoff.exhausted` and resolve with
   *      `exhausted: true`.
   */
  async execute(call: SchedulerCall): Promise<ExecutionResult> {
    const idempotency_key = call.idempotency_key ?? this.uuid();
    const max_attempts = call.max_attempts ?? defaultMaxAttempts(call);
    const isRetryable = call.is_retryable ?? defaultIsRetryable;

    // Pre-call dedupe check.
    const cached = await this.sync_op.getIfPresent(call.connector_id, idempotency_key);
    if (cached !== null) {
      return {
        result: { status: cached.status, headers: {}, body: cached.result },
        attempts: 0,
        retried: false,
        exhausted: false,
        idempotency_key,
        cached: true,
        backoff_ms: [],
      };
    }

    const backoff_ms: number[] = [];
    let lastResult: PlatformCallResultForBackoff | null = null;
    for (let attempt = 0; attempt < max_attempts; attempt++) {
      const result = await call.execute(idempotency_key);
      lastResult = result;
      const retriable = isRetryable(result);
      const has_budget = attempt < max_attempts - 1;
      if (!retriable || !has_budget) {
        // Final outcome — persist and resolve.
        await this.sync_op.put(call.connector_id, idempotency_key, {
          result: result.body,
          status: result.status,
          claimed_at_ms: this.now(),
        });
        // `exhausted` is true only when we actually performed at least
        // one retry (i.e. `backoff_ms.length > 0`) and the final
        // outcome is still not a success. A single-shot POST that
        // fails on the first attempt is NOT "exhausted" — it never
        // tried to retry.
        const exhausted = backoff_ms.length > 0 && retriable && !isSuccess(result);
        if (exhausted) {
          this.audit.emit(
            makeEvent(
              'connector.backoff.exhausted',
              call.tenant_id,
              call.platform,
              {
                idempotency_key,
                attempts: attempt + 1,
                final_status: result.status,
                backoff_ms: backoff_ms.slice(),
                connector_id: call.connector_id,
                verb: call.verb,
                metadata: call.metadata ?? {},
              },
              () => new Date(this.now()),
            ),
          );
        }
        return {
          result,
          attempts: attempt + 1,
          retried: backoff_ms.length > 0,
          exhausted,
          idempotency_key,
          cached: false,
          backoff_ms,
        };
      }
      // Compute backoff and sleep.
      const delay = this.policy.nextDelayMs(attempt, result.headers);
      backoff_ms.push(delay);
      this.audit.emit(
        makeEvent(
          'connector.backoff.retried',
          call.tenant_id,
          call.platform,
          {
            idempotency_key,
            attempt: attempt + 1,
            next_attempt: attempt + 2,
            backoff_ms: delay,
            status: result.status,
            connector_id: call.connector_id,
            verb: call.verb,
            metadata: call.metadata ?? {},
          },
          () => new Date(this.now()),
        ),
      );
      await this.sleep(delay);
    }
    // Unreachable in practice (the loop always returns) — but kept for
    // the type checker. `lastResult` is set by the loop's last iteration.
    return {
      result: lastResult ?? { status: 0, headers: {}, body: null },
      attempts: max_attempts,
      retried: backoff_ms.length > 0,
      exhausted: true,
      idempotency_key,
      cached: false,
      backoff_ms,
    };
  }

  /**
   * Enqueue a call for deferred execution. The call is added to the
   * tenant-weighted FIFO; `drain()` (or `pull()` + manual `execute()`)
   * is the consumer. Returns the enqueue position for diagnostics.
   */
  enqueue(call: SchedulerCall): { tenant_id: string; position: number } {
    this.queue.enqueue(call.tenant_id, call);
    const counts = this.queue.pendingByTenant();
    return { tenant_id: call.tenant_id, position: counts[call.tenant_id] ?? 0 };
  }

  /** Round-robin pull from the queue. Returns null when empty. */
  pull(): { tenant_id: string; call: SchedulerCall } | null {
    const next = this.queue.pull();
    if (next === null) return null;
    return { tenant_id: next.tenant_id, call: next.item };
  }

  /**
   * Drain the queue, executing each call in round-robin order.
   * Returns the per-call results and the number of items drained.
   */
  async drain(): Promise<{ drained: number; results: ExecutionResult[] }> {
    const results: ExecutionResult[] = [];
    while (true) {
      const next = this.pull();
      if (next === null) break;
      const r = await this.execute(next.call);
      results.push(r);
    }
    return { drained: results.length, results };
  }
}

function isSuccess(result: PlatformCallResultForBackoff): boolean {
  return result.status >= 200 && result.status < 300;
}

