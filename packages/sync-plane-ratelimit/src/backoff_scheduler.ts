/**
 * BackoffScheduler — retry orchestrator with tenant-weighted FIFO.
 *
 * FORA-487.3 / FORA-517. The scheduler is the third leg of the FORA-487
 * outbound-reliability stool (alongside the three-layer limiter in
 * `outbound.ts` and the composite-edit coalescer in `coalescer.ts`):
 *
 *   1. `BackoffPolicy` decides *when* to retry (Retry-After header or
 *      exponential-with-jitter schedule, clamped to [floor, ceiling]).
 *   2. `SyncOpStore` is the FORA-401 `sync_op` dedupe spine: pre-call
 *      check, post-call write, replays return the cached outcome.
 *   3. `TenantWeightedFifo` is the fairness guard: a noisy tenant that
 *      is mid-retry-burst cannot starve a quiet tenant of its turn.
 *
 * The audit module receives two new event types on this layer:
 *   - `connector.backoff.retried`      — successful retry after backoff
 *   - `connector.backoff.exhausted`    — retry budget exhausted
 *
 * The `execute()` path is the synchronous-looking entry point: it
 * sleeps between attempts (configurable `sleep` injection for tests)
 * and resolves with the final outcome. The `enqueue()` + `drain()`
 * path is the producer/consumer path — useful when the rate limiter
 * has rejected an op and we want to defer delivery to a later tick.
 *
 * The scheduler is transport-agnostic. It does NOT make HTTP calls
 * itself — the caller injects an `execute` function that wraps the
 * platform adapter (Jira / GitHub / ClickUp). This keeps the package
 * free of network state and lets tests run without a mock HTTP server.
 */

import { BackoffPolicy } from './backoff_policy.js';
import { uuidV7 } from './idempotency_key.js';
import { InMemorySyncOpStore, type SyncOpStore } from './sync_op_store.js';
import { InMemoryAuditSink, NoopAuditSink, makeEvent, type AuditSink } from './audit.js';
import type { PlatformId } from './coalescer.js';

