class OIDCCallbackRequest(BaseModel):
    """Body for ``POST /auth/oidc/callback``.

    The frontend posts the Keycloak authorization code together with
    the PKCE verifier it generated before redirecting to Keycloak.
    """

    code: str = Field(..., min_length=1, description="Authorization code from Keycloak")
    redirect_uri: str = Field(
        ...,
        min_length=1,
        description="Same redirect_uri used in the original /auth request",
    )
    code_verifier: str = Field(
        ...,
        min_length=43,
        max_length=128,
        description="PKCE verifier generated by the SPA",
    )


class TokenResponse(BaseModel):
    """Successful OIDC callback response.

    ``proxy_token`` (step-65) is the RS256 JWT the backend (or, if a
    future flow asks for it, the SPA itself) sends as
    ``Authorization: Bearer`` when calling ``/v1/chat/completions`` on
    the LiteLLM Proxy.  The proxy trusts the proxy token via its
    ``enable_jwt_auth`` block; we mint it once at login and cache it
    in Redis keyed on the access-token fingerprint so subsequent
    requests don't pay the sign cost.
    """

    access_token: str
    refresh_token: str
    proxy_token: str | None = None
    token_type: str = "bearer"
    expires_in: int = int(_ACCESS_TOKEN_TTL.total_seconds())
    user: dict[str, Any]
    tenant: dict[str, Any]


class RefreshRequest(BaseModel):
    refresh_token: str = Field(..., min_length=1)


class AccessTokenResponse(BaseModel):
    access_token: str
    proxy_token: str | None = None
    token_type: str = "bearer"
    expires_in: int = int(_ACCESS_TOKEN_TTL.total_seconds())


