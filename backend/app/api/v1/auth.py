# Keycloak endpoint discovery. We hit the same paths on every
# environment; only ``KEYCLOAK_URL`` / ``KEYCLOAK_REALM`` change.
def _keycloak_token_url() -> str:
    return (
        f"{settings.keycloak_url}/realms/{settings.keycloak_realm}"
        "/protocol/openid-connect/token"
    )


def _keycloak_userinfo_url() -> str:
    return (
        f"{settings.keycloak_url}/realms/{settings.keycloak_realm}"
        "/protocol/openid-connect/userinfo"
    )


# Forge token claims. ``type`` separates access vs refresh tokens so the
# refresh endpoint can refuse to mint new tokens from a leaked access
# token (and vice versa).
_ACCESS_TOKEN_TTL = timedelta(hours=1)
_REFRESH_TOKEN_TTL = timedelta(days=7)
_TOKEN_TYPE_ACCESS = "access"
_TOKEN_TYPE_REFRESH = "refresh"

# Keycloak user attributes that drive our tenant bootstrap.
_ATTR_TENANT_ID = "tenant_id"
_ATTR_TENANT_SLUG = "tenant_slug"
_ATTR_TENANT_NAME = "tenant_name"
_ATTR_ROLE = "role"

# Fallback tenant used when a Keycloak user has no tenant attributes
# (e.g. realm admin, integration user). Keep the slug stable so the
# downstream code never has to special-case ``None``.
_FALLBACK_TENANT_ID = "00000000-0000-0000-0000-000000000000"
_FALLBACK_TENANT_SLUG = "default"
_FALLBACK_TENANT_NAME = "Default Tenant"


