def _keycloak_jwks_url() -> str:
    return (
        f"{settings.keycloak_url}/realms/{settings.keycloak_realm}"
        "/protocol/openid-connect/certs"
    )


# Cache the JWKS in-process so every callback doesn't refetch the key set.
# The cache lives for the lifetime of the backend process; Keycloak rotates
# keys rarely (and the rotation triggers a /realms/.../certs cache miss
# that we re-fetch on the next login — at worst, one user pays the cost
# of the fetch, everyone else gets the cached keys).
_JWKS_CACHE: dict[str, Any] = {"keys": None, "fetched_at": 0.0}
_JWKS_CACHE_TTL_SECONDS = 300.0


async def _get_jwks() -> list[dict[str, Any]]:
    """Return the cached Keycloak JWKS, refreshing at most every 5 min."""
    import time

    now = time.monotonic()
    if _JWKS_CACHE["keys"] is not None and (now - _JWKS_CACHE["fetched_at"]) < _JWKS_CACHE_TTL_SECONDS:
        return _JWKS_CACHE["keys"]

    async with httpx.AsyncClient(timeout=10.0) as client:
        resp = await client.get(_keycloak_jwks_url())
    if resp.status_code != 200:
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail="oidc_provider_unavailable",
        )
    keys = resp.json().get("keys", [])
    _JWKS_CACHE["keys"] = keys
    _JWKS_CACHE["fetched_at"] = now
    return keys


async def _decode_keycloak_id_token(id_token: str) -> dict[str, Any]:
    """Verify an OIDC ID token signature + decode its claims.

    Validation steps:
      1. Fetch Keycloak's JWKS (cached).
      2. Locate the key whose ``kid`` matches the ID token header's
         ``kid``.
      3. Verify the signature using ``python-jose`` against that key.
      4. Reject if ``aud`` doesn't include ``forge-ui`` (defends against
         an attacker swapping in a token issued for a different client).
      5. Reject if ``iss`` doesn't end with
         ``/realms/{realm}`` (defends against cross-realm token replay).

    Issuer matching is **path-based** rather than full-string, because
    the browser hits Keycloak via ``http://localhost:8080`` while the
    backend hits it via ``http://keycloak:8080``. Same realm, two
    hostnames; Keycloak happily issues tokens with the request's
    effective issuer. Strict full-string match would falsely reject
    browser-issued tokens.
    """
    try:
        header = jwt.get_unverified_header(id_token)
        kid = header.get("kid")
        if not kid:
            raise JWTError("missing kid")
        keys = await _get_jwks()
        key = next((k for k in keys if k.get("kid") == kid), None)
        if key is None:
            raise JWTError(f"no JWKS key for kid={kid}")
        # Disable jose's built-in issuer check; we do path-based
        # validation below.
        claims = jwt.decode(
            id_token,
            key,
            algorithms=[key.get("alg", "RS256")],
            audience="forge-ui",
            options={
                "verify_at_hash": False,
                "verify_iss": False,
            },
        )
        # Path-based issuer check: must end with /realms/{realm}.
        iss = claims.get("iss", "")
        expected_suffix = f"/realms/{settings.keycloak_realm}"
        if not (isinstance(iss, str) and iss.endswith(expected_suffix)):
            raise JWTError(f"Invalid issuer: {iss!r}")
    except JWTError as exc:
        logger.warning("auth.oidc.invalid_id_token", error=str(exc))
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="invalid_id_token",
        ) from exc

    return claims


def _first_attr(userinfo: dict[str, Any], key: str) -> str | None:
    """Return the first value of a Keycloak user attribute.

    Keycloak user attributes come back as lists (even single-valued
    ones) so we collapse to the head element when present. Returns
    ``None`` for absent / empty values so the caller can apply
    fallbacks.
    """
    value = userinfo.get(key)
    if isinstance(value, list):
        return value[0] if value else None
    return value or None


