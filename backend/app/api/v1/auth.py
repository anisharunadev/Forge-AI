@router.post("/refresh", response_model=AccessTokenResponse)
async def refresh(req: RefreshRequest) -> AccessTokenResponse:
    """Trade a Forge refresh token for a fresh access token.

    Returns 401 (``not_a_refresh_token`` / ``invalid_refresh_token``) on
    any failure. The frontend treats both as "kick the user back to
    /login" — there's no recovery flow that doesn't require the user to
    re-authenticate through Keycloak.
    """
    try:
        payload = decode_token(req.refresh_token)
    except HTTPException:
        # decode_token already shaped the error as 401; re-raise.
        raise

    if payload.get("type") != _TOKEN_TYPE_REFRESH:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="not_a_refresh_token",
        )

    user_id = payload.get("sub")
    tenant_id = payload.get("forge.tenant") or payload.get("tenant_id")
    if not user_id or not tenant_id:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="invalid_refresh_token",
        )

    # Confirm the principal still exists in this tenant. A deleted user
    # shouldn't be able to keep refreshing forever.
    factory = get_session_factory()
    async with factory() as session:
        user = await get_user_by_id(session, user_id=user_id, tenant_id=tenant_id)
    if user is None:
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="invalid_refresh_token",
        )

    now = datetime.now(tz=timezone.utc)
    access_claims = {
        "sub": user["id"],
        "email": user["email"],
        "forge.tenant": tenant_id,
        "role": user["role"],
        "type": _TOKEN_TYPE_ACCESS,
        "iat": now,
        "exp": now + _ACCESS_TOKEN_TTL,
    }
    forge_access = jwt.encode(
        access_claims,
        settings.jwt_secret,
        algorithm=settings.jwt_algorithm,
    )

    # step-65: re-issue the proxy_token alongside the access token so
    # the LiteLLM Proxy keeps trusting the user after a refresh.
    proxy_token_value = issue_proxy_token(
        user_id=user["id"],
        email=user["email"],
        tenant_id=tenant_id,
        project_id=None,
        roles=[user["role"]],
    )
    await proxy_token_cache.store(
        forge_access,
        proxy_token_value,
        ttl_seconds=int(_ACCESS_TOKEN_TTL.total_seconds()),
    )

    return AccessTokenResponse(
        access_token=forge_access,
        proxy_token=proxy_token_value,
    )
